struct GPUvertice {
    var position:SIMD3<Float>
    var color:SIMD4<Float>
    var uv:SIMD2<Float>
    var normal:SIMD3<Float>
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////
struct GPUverticeBone {
    var position:SIMD3<Float>
    var color:SIMD4<Float>
    var uv:SIMD2<Float>
    var normal:SIMD3<Float>
    var bone0:Int32
    var bone1:Int32
    var weight0:Float32
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
struct GPUinstance {
    var matrix:simd_float4x4
    var color:SIMD4<Float>
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////
struct GPUmaterial {
    var ambient:SIMD4<Float>
    var diffuse:SIMD4<Float>
    var specular:SIMD4<Float>
    var shininess:Float32
}
